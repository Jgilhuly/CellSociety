package cellsociety_team01;

public class Pair {

	private int myX;
	private int myY;

	public Pair(int x, int y){
		this.myX = x;
		this.myY = y;
	}

	public int getX() {
		return myX;
	}
	public int getY() {
		return myY;
	}
	public void setX(int x) {
		this.myX = x;
	}
	public void setR(int y) {
		this.myY = y;
	}
}